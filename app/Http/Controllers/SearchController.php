     */
    public function index(Request $request)
    {
        $nft = new Collectible;

        $data = $nft->fetchAllCollectibles(null, 'updated_at', 'asc', $request->input('nft'));

        $this->data['collectibles'] = json_decode(json_encode($data));

        return view('search', $this->data);
    }

